function actionScore(action: GeneratedRecommendedAction) {
  const priority = action.priority === "high" ? 30 : action.priority === "medium" ? 20 : 10;
  const business = action.type === "data_quality_action" ? 0 : 25;
  const objects = (action.targetObjects?.length ?? 0) * 4;
  const evidence = (action.evidenceRankings?.length ?? 0) * 5 + (action.evidenceMetrics?.length ?? 0) * 2;
  const caveatPenalty = Math.min(action.caveats?.length ?? 0, 3) * 2;

  return priority + business + objects + evidence - caveatPenalty;
}

