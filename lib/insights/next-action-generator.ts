function allRankings(aggregations: AggregationResult[]) {
  return aggregations.flatMap((aggregation) => [
    ...aggregation.topRankings,
    ...aggregation.bottomRankings,
    ...aggregation.riskCandidates.flatMap((candidate) => candidate.affectedObjects?.length || candidate.objects?.length ? [] : []),
    ...aggregation.opportunityCandidates.flatMap((candidate) => candidate.targetObjects?.length || candidate.objects?.length ? [] : [])
  ]).filter((item): item is RankingResult => "rows" in item);
}

function objectName(row: Record<string, string | number | null>, index = 0) {
  return String(
    row.App ??
    row.Product ??
    row.product ??
    row.Category ??
    row.category ??
    row.dimension ??
    row.group ??
    row.Group ??
    `对象 ${index + 1}`
  );
}

function namesFromRows(rows?: Array<Record<string, string | number | null>>, count = 3) {
  return (rows ?? [])
    .slice(0, count)
    .map((row, index) => objectName(row, index))
    .filter(Boolean);
}

function bestRanking(aggregations: AggregationResult[], types: Array<NonNullable<RankingResult["rankingType"]>>) {
  const rankings = allRankings(aggregations);

  return types.flatMap((type) => rankings.filter((ranking) => ranking.rankingType === type))[0] ?? rankings[0];
}

function caveatType(limitation: DataLimitation): ActionCaveat["type"] {
  const text = `${limitation.id} ${limitation.title ?? ""} ${limitation.message}`.toLowerCase();
  if (text.includes("estimated") || text.includes("估算")) return "estimated_value";
  if (text.includes("dedup") || text.includes("去重") || text.includes("重复")) return "deduplication";
  if (text.includes("sample") || text.includes("样本")) return "small_sample";
  if (text.includes("time") || text.includes("趋势")) return "missing_trend";
  if (text.includes("benchmark") || text.includes("基准")) return "missing_benchmark";
  if (text.includes("raw") || text.includes("原始")) return "raw_metric";

  return "missing_field";
}

function limitationToCaveat(limitation: DataLimitation): ActionCaveat {
  const type = caveatType(limitation);

  return {
    id: `caveat-${limitation.id}`,
    type,
    message: limitation.limitation ?? limitation.message,
    affectedMetrics: limitation.title ? [limitation.title] : [],
    displayMode: type === "estimated_value" || type === "deduplication" ? "badge" : "collapsed_detail"
  };
}

