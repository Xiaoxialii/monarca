export function generateNextActionPlan(input: NextActionInput): GeneratedNextActionPlan {
  const caveats = input.dataLimitations.map(limitationToCaveat);
  const missingDataRequests = uniqueById(input.dataLimitations.map(missingRequestFromLimitation).filter((item): item is MissingDataRequest => Boolean(item)));
  const autoGeneratedResults = autoGeneratedResultsFromInput(input);
  const actionCandidates = uniqueById([
    ...actionInsightsFromRecommended(input),
    ...input.businessRisks.slice(0, 2).map((risk, index) => actionFromRisk(input, risk, index)),
    ...input.growthOpportunities.slice(0, 2).map(actionFromOpportunity),
    actionFromRanking(input, autoGeneratedResults),
    fallbackAction(input, autoGeneratedResults)
  ].filter((item): item is ActionInsight => Boolean(item)));
  const dedupedActions = dedupeActionInsights(actionCandidates);
  const refinedActions = dedupeActionInsights(dedupedActions.map((action) => refineActionInsight(action, input)));
  const businessActions = refinedActions.filter((action) => !isDataActionType(action.actionType)).slice(0, 3);
  const dataActions = refinedActions.filter((action) => isDataActionType(action.actionType)).slice(0, 2);
  const actionInsights = [...businessActions, ...dataActions].slice(0, 5);

  return {
    autoGeneratedResults,
    actionInsights,
    priorityActions: actionInsights,
    missingDataRequests: missingDataRequests.slice(0, 5),
    caveats: caveats.slice(0, 8)
  };
}
