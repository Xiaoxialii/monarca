function mapActionType(action: GeneratedRecommendedAction, businessType?: ReportBusinessType): ActionInsight["actionType"] {
  if (action.actionType === "create_deduped_metric" || action.actionType === "build_benchmark") {
    return "fix_data_quality_for_decision";
  }
  if (action.actionType === "collect_revenue_field") return "collect_missing_business_data";
  if (action.actionType) {
    return action.actionType as ActionInsight["actionType"];
  }

  const title = `${action.title} ${action.action} ${action.recommendedAction ?? ""}`;
  if (/负向|评论|反馈/.test(title)) return "reduce_negative_feedback";
  if (/机会|放大|增长/.test(title)) return "scale_opportunity_object";
  if (/ROI|收入|变现|估算/.test(title)) return "validate_roi";
  if (businessType === "finance_timeseries") return "investigate_anomaly";
  if (businessType === "marketing") return "reallocate_budget";
  if (businessType === "user_growth" || businessType === "product_usage") return "improve_retention";

  return "optimize_risk_object";
}

