function normalizeMetricKey(value: string) {
  return value.toLowerCase().replace(/[^a-z0-9]+/g, "");
}

function formatActionNumber(value: number, context = "") {
  if (!Number.isFinite(value)) return "";

  if (/rate|ratio|share|率|占比/i.test(context) && Math.abs(value) <= 1) {
    return `${(value * 100).toFixed(1)}%`;
  }

  const absolute = Math.abs(value);
  if (absolute >= 1_000_000_000) return `${(value / 1_000_000_000).toFixed(2)}B`;
  if (absolute >= 1_000_000) return `${(value / 1_000_000).toFixed(2)}M`;
  if (absolute >= 1_000) return `${(value / 1_000).toFixed(1)}K`;

  return Number.isInteger(value) ? String(value) : value.toFixed(2);
}

function formatActionValue(name: string, value: string | number | null | undefined) {
  if (value == null || value === "") return "";
  if (typeof value === "number") return formatActionNumber(value, name);

  return value;
}

function evidenceLookup(input: NextActionInput) {
  const values = new Map<string, string>();

  for (const insight of input.keyFindings) {
    for (const [key, value] of Object.entries(insight.evidenceValues ?? {})) {
      const formatted = formatActionValue(key, value);
      if (formatted) values.set(normalizeMetricKey(key), formatted);
    }
  }

  for (const risk of input.businessRisks) {
    for (const [key, value] of Object.entries(risk.evidenceValues ?? {})) {
      const formatted = formatActionValue(key, value);
      if (formatted) values.set(normalizeMetricKey(key), formatted);
    }
  }

  for (const opportunity of input.growthOpportunities) {
    for (const [key, value] of Object.entries(opportunity.evidenceValues ?? {})) {
      const formatted = formatActionValue(key, value);
      if (formatted) values.set(normalizeMetricKey(key), formatted);
    }
  }

  return values;
}

function findEvidenceValue(input: NextActionInput, names: string[]) {
  const lookup = evidenceLookup(input);

  for (const name of names) {
    const exact = lookup.get(normalizeMetricKey(name));
    if (exact) return exact;
  }

  for (const [key, value] of lookup) {
    if (names.some((name) => key.includes(normalizeMetricKey(name)))) return value;
  }

  return "";
}

function metricUnitLabel(metric = "") {
  if (/install/i.test(metric)) return "installs";
  if (/review/i.test(metric)) return "reviews";
  if (/volume/i.test(metric)) return "volume";

  return "";
}

function rowMetricValue(row: Record<string, string | number | null>, metric?: string) {
  if (metric && row[metric] != null) return row[metric];
  const candidates = ["value", "Installs", "installs", "Reviews", "reviews", "rating", "Rating", "score", "Score"];

  for (const key of candidates) {
    if (row[key] != null) return row[key];
  }

  return null;
}

function rankingEvidenceText(ranking?: RankingResult, count = 3) {
  if (!ranking?.rows.length) return "";

  const metric = ranking.metricField ?? ranking.metric ?? "";
  const unit = metricUnitLabel(metric);

  return ranking.rows.slice(0, count).map((row, index) => {
    const name = objectName(row, index);
    const value = formatActionValue(metric, rowMetricValue(row, metric));

    return value ? `${name}: ${value}${unit ? ` ${unit}` : ""}` : name;
  }).join("；");
}

